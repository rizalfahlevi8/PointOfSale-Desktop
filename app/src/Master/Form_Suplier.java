    private void GetData() {
        try {
            Connection conn = Koneksi.getConnection();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("SELECT * FROM supplier");
            tbl_supplier.setModel(DbUtils.resultSetToTableModel(sql));
            tbl_supplier.getColumnModel().getColumn(0).setPreferredWidth(35);
            tbl_supplier.getColumnModel().getColumn(1).setPreferredWidth(100);
            tbl_supplier.getColumnModel().getColumn(2).setPreferredWidth(90);
            tbl_supplier.getColumnModel().getColumn(3).setPreferredWidth(80);

            //sql.last();
            String count_rows = String.valueOf(tbl_supplier.getRowCount());
            lblcount_rows.setText("Jumlah Data : " + count_rows);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error " + e);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Form_User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void GetData_View(){
        int row = tbl_supplier.getSelectedRow();
        String row_id = (tbl_supplier.getModel().getValueAt(row, 0).toString());
        txt_id.setText(row_id);
        BtnEnabled(true);
    }
    
    private void cariSupplier() {
        try {
            Connection conn = Koneksi.getConnection();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("SELECT * FROM supplier"
            + " WHERE supplier.supplier_Id LIKE '%" + txt_cariSupplier.getText() 
                    + "%' || supplier.nama_supplier LIKE '%" + txt_cariSupplier.getText() 
                    + "%' || supplier.alamat_supplier LIKE '%" + txt_cariSupplier.getText()
                    + "%' || supplier.telp_supplier LIKE '%" + txt_cariSupplier.getText() + "%'");
            tbl_supplier.setModel(DbUtils.resultSetToTableModel(sql));
            tbl_supplier.getColumnModel().getColumn(0).setPreferredWidth(35);
            tbl_supplier.getColumnModel().getColumn(1).setPreferredWidth(100);
            tbl_supplier.getColumnModel().getColumn(2).setPreferredWidth(90);
            tbl_supplier.getColumnModel().getColumn(3).setPreferredWidth(80);

            //sql.last();
            String count_rows = String.valueOf(tbl_supplier.getRowCount());
            lblcount_rows.setText("Jumlah Data : " + count_rows);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error " + e);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Form_User.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

